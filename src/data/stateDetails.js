export const getStateDetails = (stateCode) => {
  return stateDetails[stateCode] || null;
};

export const getAllStates = () => {
  return Object.keys(stateDetails);
};

export const getStatesByCircuit = (circuit) => {
  return Object.entries(stateDetails)
    .filter(([_, data]) => data.circuit === circuit)
    .map(([code, _]) => code);
};

export const getStatesByHostility = (hostility) => {
  return Object.entries(stateDetails)
    .filter(([_, data]) => data.hostility === hostility)
    .map(([code, _]) => code);
};